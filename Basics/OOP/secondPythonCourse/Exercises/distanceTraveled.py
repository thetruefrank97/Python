# Assuming there are no accidents or delays, the distance that a car travels
# down the inter-state can be calculated with the following formula:
#
# Distance = Speed * Timeo
#
# A car is traveling at 70 miles per hour. Write a program that displays the
# following:
# The distance the car will travel in 6 hours
# The distance the car will travel in 10 hours
# The distance the car will travel in 15 hours

speed = 70
print("In 6 hours, the car will travel  " + str((speed * 6)) + " miles")
print("In 6 hours, the car will travel  " + str((speed * 10)) + " miles")
print("In 6 hours, the car will travel  " + str((speed * 15)) + " miles")